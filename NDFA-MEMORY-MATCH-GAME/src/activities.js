export function getRandomActivity(category) {
  const cat = ACTIVITY_CATEGORIES[category]
  if (!cat || !cat.activities.length) return { title: category, text: 'בחר פעילות מהרשימה.' }
  const text = cat.activities[Math.floor(Math.random() * cat.activities.length)]
  return { title: cat.title, emoji: cat.emoji, text }
}

export function getCardEmoji(category) {
  return ACTIVITY_CATEGORIES[category]?.emoji || '❓'
}
